@remote_app.command()
def sandbox(
    repo: str = typer.Argument(..., help="Git repository URL to clone and sandbox"),
    host: str = typer.Option(
        None, "--host", "-H", help="SSH connection (user@host). Uses saved default if omitted."
    ),
    env_file: str = typer.Option(None, "--env-file", "-e", help="Local .env file to upload"),
    env: list[str] = typer.Option([], "--env", help="Environment variable (KEY=VALUE), repeatable"),
    key: str = typer.Option(None, "--key", "-k", help="Path to SSH private key"),
    port: int = typer.Option(22, "--port", "-p", help="SSH port"),
    json_output: bool = typer.Option(False, "--json", help="Output structured JSON"),
    yes: bool = typer.Option(False, "--yes", "-y", help="Skip approval prompt"),
) -> None:
    """Clone a repo on a remote machine and spin up a production sandbox."""
    conn = _resolve_host(host, key, port)
    if not conn:
        return

    console.print("\n  [bold]Remote sandbox[/bold]")
    rc_print(f"  Host: {conn.label}", style="rc.dim")
    rc_print(f"  Repo: {repo}", style="rc.dim")
    console.print()

    # Auto-provision if the remote isn't ready
    rc_print("  Checking remote environment...")
    if not _ensure_provisioned(conn):
        return
    rc_success("Remote environment ready")

    # Run the sandbox
    ok = _run_sandbox(conn, repo, env_file, env, json_output, yes)

    if json_output:
        remote_dir = provisioner.load_active_project()
        if remote_dir:
            r = conn.run(f"cat {remote_dir}/.raincurve/run_state.json 2>/dev/null")
            if r.ok:
                print(r.stdout)
    else:
        if ok:
            rc_success("Remote sandbox is running!")
            console.print()
            rc_print("  To reproduce issues:")
            rc_print(
                '  raincurve remote reproduce "describe the bug here"',
                style="rc.dim",
            )
            rc_print("\n  To tear down:")
            rc_print("  raincurve remote teardown", style="rc.dim")
        else:
            rc_error("Sandbox failed — check the output above.")
            remote_dir = provisioner.load_active_project()
            if remote_dir and not json_output:
                console.print()
                if Confirm.ask("  Drop into interactive chat to guide the agent?", default=True):
                    rc_print("  Starting interactive session (Ctrl+C to exit)...\n")
                    chat_cmd = (
                        f"cd {remote_dir} && export PATH=$HOME/.local/bin:$PATH && raincurve chat"
                    )
                    conn.run_interactive(chat_cmd)

    console.print()


