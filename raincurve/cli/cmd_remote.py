@remote_app.command()
def setup(
    host: str = typer.Argument(..., help="SSH connection string (user@host or user@host:port)"),
    key: str = typer.Option(None, "--key", "-k", help="Path to SSH private key"),
    port: int = typer.Option(22, "--port", "-p", help="SSH port"),
) -> None:
    """Connect to a remote machine, verify Docker, and install raincurve."""
    conn = RemoteHost.from_string(host, key_path=key, port=port)
    console.print(f"\n  [bold]Remote setup:[/bold] {conn.label}\n")

    # 1. SSH connectivity
    rc_print("  Testing SSH connection...")
    r = conn.test()
    if not r.ok:
        rc_error(f"SSH failed: {r.stderr}")
        rc_print("  Check your host, username, key, and port.", style="rc.dim")
        return
    rc_success("SSH connected")

    # 2. Prerequisites
    rc_print("  Checking prerequisites...")
    checks = provisioner.check_prerequisites(conn)

    table = Table(show_header=False, padding=(0, 2))
    table.add_column("Check", style="bold", width=20)
    table.add_column("Status")

    docker_ok = checks.get("docker", False)
    git_ok = checks.get("git", False)
    disk = checks.get("disk_gb", 0)
    ram = checks.get("ram_gb", 0)

    table.add_row(
        "Docker",
        f"[green]v{checks.get('docker_version', '?')}[/]" if docker_ok else "[red]Not found[/]",
    )
    table.add_row("Git", "[green]Available[/]" if git_ok else "[red]Not found[/]")
    table.add_row(
        "Disk",
        f"[green]{disk:.0f} GB free[/]"
        if disk >= 10
        else f"[yellow]{disk:.0f} GB free (need 10+)[/]",
    )
    table.add_row(
        "RAM", f"[green]{ram} GB[/]" if ram >= 4 else f"[yellow]{ram} GB (recommend 4+)[/]"
    )
    console.print(table)

    # 3. Install Docker if missing
    if not docker_ok:
        if Confirm.ask("\n  Docker not found. Install it?", default=True):
            rc_print("  Installing Docker (this takes a minute)...")
            r = provisioner.install_docker(conn)
            if r.ok:
                rc_success("Docker installed")
            else:
                rc_error(f"Docker install failed: {r.stderr}")
                return
        else:
            rc_error("Docker is required. Setup cannot continue.")
            return

    # 4. Install raincurve
    rc_print("  Installing raincurve on remote...")
    r = provisioner.install_raincurve(conn)
    if r.ok and "raincurve" in r.stdout.lower():
        rc_success(f"raincurve installed ({r.stdout.splitlines()[-1].strip()})")
    else:
        rc_warn(f"raincurve install may have issues: {r.stdout[-200:]}")
        rc_print("  Continuing anyway — the sandbox command will verify.", style="rc.dim")

    # 5. Configure LLM on remote
    cfg = load_global_config()
    provider = cfg.llm.provider or "openrouter"
    api_key = cfg.llm.api_key or cfg.llm.openrouter_api_key or ""

    if api_key:
        rc_print(f"  Configuring LLM ({provider}) on remote...")
        r = provisioner.configure_llm(conn, provider, api_key)
        if r.ok:
            rc_success("LLM configured on remote")
        else:
            rc_warn(f"LLM config issue: {r.stderr}")
    else:
        rc_warn("No LLM API key found locally. Run `raincurve init` first, then re-run setup.")

    # 6. Save remote config
    provisioner.save_remote_config(conn)
    rc_success("Remote host saved as default")

    console.print()
    rc_success("Setup complete!")
    rc_print(
        "  Next: raincurve remote sandbox <repo-url> --env-file .env\n",
        style="rc.dim",
    )


