@remote_app.command()
def reproduce(
    description: str = typer.Argument(..., help="Bug description — what to reproduce"),
    host: str = typer.Option(
        None, "--host", "-H", help="SSH connection. Uses saved default if omitted."
    ),
    repo: str = typer.Option(
        None, "--repo", "-r", help="Git repo URL — auto-provisions sandbox if not running."
    ),
    env_file: str = typer.Option(
        None, "--env-file", "-e", help="Local .env file (used if auto-provisioning)."
    ),
    env: list[str] = typer.Option(
        [], "--env", help="Environment variable KEY=VALUE (used if auto-provisioning), repeatable."
    ),
    project: str = typer.Option(
        None, "--project", help="Remote project directory. Uses last sandbox if omitted."
    ),
    key: str = typer.Option(None, "--key", "-k", help="Path to SSH private key"),
    port: int = typer.Option(22, "--port", "-p", help="SSH port"),
    json_output: bool = typer.Option(False, "--json", help="Output structured JSON"),
) -> None:
    """Reproduce a bug in the running remote sandbox and generate a report."""
    conn = _resolve_host(host, key, port)
    if not conn:
        return

    remote_dir = project or provisioner.load_active_project()

    console.print("\n  [bold]Remote reproduce[/bold]")
    rc_print(f"  Host: {conn.label}", style="rc.dim")
    rc_print(f"  Issue: {description[:100]}", style="rc.dim")
    console.print()

    # Auto-provision + auto-sandbox if nothing is running
    rc_print("  Checking sandbox status...")
    sandbox_running = False
    if remote_dir:
        status = provisioner.get_sandbox_status(conn, remote_dir)
        sandbox_running = status is not None

    if not sandbox_running:
        if not repo:
            rc_error(
                "No running sandbox and no --repo provided.",
                hint="Use --repo <url> to auto-provision, or run `raincurve remote sandbox` first.",
            )
            return

        rc_warn("No sandbox running — provisioning automatically...")
        console.print()

        if not _ensure_provisioned(conn):
            return

        ok = _run_sandbox(conn, repo, env_file, env, json_output=False, yes=False)
        if not ok:
            rc_error("Sandbox provisioning failed — cannot reproduce.")
            return

        remote_dir = provisioner.load_active_project()
        if not remote_dir:
            rc_error("Could not determine remote project directory after provisioning.")
            return

        rc_success("Sandbox provisioned — now reproducing the issue...")
        console.print()
    else:
        rc_success("Sandbox is running")
        rc_print(f"  Project: {remote_dir}", style="rc.dim")

    # Build the reproduce prompt — deep investigation, not just endpoint probing
    prompt = (
        f"ISSUE TO INVESTIGATE:\n{description}\n\n"
        "You are investigating a bug in a live application. Do NOT just hit API endpoints "
        "and look for HTTP errors. Many bugs are data correctness issues — wrong values, "
        "missing records, incorrect calculations, logic errors that return 200 OK with bad data.\n\n"
        "INVESTIGATION PROCESS:\n"
        "1. READ THE CODE FIRST. Use bash to grep and cat the source files. Find the code "
        "path that handles the described behavior. Identify the relevant files, functions, "
        "and database queries.\n"
        "2. QUERY THE DATABASE. Use the database CLI tools available in the containers "
        "(psql, mysql, mongosh, etc.) to inspect the actual data. Run the same queries the "
        "code runs and check if the results are correct.\n"
        "3. CHECK APPLICATION LOGS. Read container logs with docker logs. Look for warnings, "
        "errors, or unexpected values.\n"
        "4. TRIGGER THE BUG. Hit the relevant endpoints or run the relevant code paths with "
        "inputs that should trigger the issue. Compare actual output vs expected output.\n"
        "5. TRACE THE ROOT CAUSE. Follow the code from input to output. Find the exact "
        "file and function where the logic breaks. Note the line numbers.\n"
        "6. DOCUMENT EVERYTHING for a coding agent to fix it.\n\n"
        "In your findings, you MUST include:\n"
        "- title: what the bug is\n"
        "- severity: critical/high/medium/low\n"
        "- evidence: the actual vs expected output you observed\n"
        "- endpoint: the API route or code path affected\n\n"
        "In your summary, include ALL of the following so a coding agent can fix this "
        "without any additional context:\n"
        "- ROOT CAUSE: exact file path(s) and function name(s) where the bug lives\n"
        "- REPRODUCTION: the exact commands (curl, psql, etc.) that trigger the bug\n"
        "- EXPECTED vs ACTUAL: what the output should be vs what it is\n"
        "- SUGGESTED FIX: what specifically needs to change in the code\n"
    )

    # Run chat agent on remote
    escaped_prompt = prompt.replace("'", "'\\''")
    chat_cmd = (
        f"cd {remote_dir} && "
        "export PATH=$HOME/.local/bin:$PATH && "
        f"raincurve chat --json '{escaped_prompt}'"
    )

    console.print()
    rc_print("  Investigating issue (this may take several minutes)...")
    rc_print("  Agent is reading code, querying databases, tracing logic...\n", style="rc.dim")
    console.print("  " + "─" * 60)

    output_lines: list[str] = []
    conn.run_stream(
        chat_cmd,
        on_line=lambda line: (output_lines.append(line), console.print(f"  {line}")),
        timeout=600,
    )

    console.print("  " + "─" * 60)
    console.print()

    # Parse JSON result from output
    result = _parse_agent_result(output_lines)
    full_output = "\n".join(output_lines)

    # Build the coding agent prompt
    coding_prompt = _build_coding_agent_prompt(description, result, full_output)

    # Save report
    reports_dir = Path.home() / ".raincurve" / "reports"
    reports_dir.mkdir(parents=True, exist_ok=True)
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H-%M-%S")

    report_data = {
        "host": conn.label,
        "project": remote_dir,
        "issue": description,
        "timestamp": ts,
        **(result or {}),
        "coding_agent_prompt": coding_prompt,
    }

    report_path = reports_dir / f"reproduce-{ts}.json"
    report_path.write_text(json.dumps(report_data, indent=2), encoding="utf-8")

    prompt_path = reports_dir / f"reproduce-{ts}.prompt.md"
    prompt_path.write_text(coding_prompt, encoding="utf-8")

    if json_output:
        print(json.dumps(report_data, indent=2))
        return

    # Display results
    if result and result.get("success"):
        verdict = result.get("verdict", "unknown")
        summary = result.get("summary", "")
        findings = result.get("findings", [])

        verdict_style = {
            "succeeded": "rc.success",
            "found_issue": "rc.warn",
            "stuck": "rc.warn",
            "errored": "rc.error",
        }.get(verdict, "rc.info")

        rc_print(f"  Verdict: [{verdict_style}]{verdict}[/{verdict_style}]")
        console.print()

        if summary:
            rc_print("[bold]  Analysis:[/bold]")
            for line in summary.split("\n"):
                rc_print(f"    {line}")
            console.print()

        if findings:
            rc_print("[bold]  Findings:[/bold]")
            for f in findings:
                sev = f.get("severity", "?")
                title = f.get("title", "?")
                evidence = f.get("evidence", "")
                endpoint = f.get("endpoint", "")

                sev_style = {
                    "critical": "red bold",
                    "high": "red",
                    "medium": "yellow",
                    "low": "cyan",
                }.get(sev, "white")

                rc_print(f"    [{sev_style}][{sev}][/{sev_style}] {title}")
                if endpoint:
                    rc_print(f"          Path: {endpoint}", style="rc.dim")
                if evidence:
                    for eline in evidence.split("\n")[:5]:
                        rc_print(f"          {eline[:200]}", style="rc.dim")
            console.print()

        duration = result.get("duration_s", 0)
        tools = result.get("tool_calls", 0)
        rc_print(f"  Duration: {duration:.1f}s | Tool calls: {tools}", style="rc.dim")
    else:
        rc_warn("Could not parse structured results from agent.")
        rc_print("  See the raw output above.", style="rc.dim")

    console.print()
    rc_print(f"  Report: {report_path}", style="rc.dim")
    rc_success(f"Coding agent prompt: {prompt_path}")
    rc_print("  Paste the .prompt.md file into Claude or Cursor to get a fix.\n", style="rc.dim")


