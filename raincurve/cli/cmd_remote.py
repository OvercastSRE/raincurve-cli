@remote_app.command()
def status(
    host: str = typer.Option(
        None, "--host", "-H", help="SSH connection. Uses saved default if omitted."
    ),
    project: str = typer.Option(None, "--project", help="Remote project directory"),
    key: str = typer.Option(None, "--key", "-k", help="Path to SSH private key"),
    port: int = typer.Option(22, "--port", "-p", help="SSH port"),
) -> None:
    """Check the status of a remote sandbox."""
    conn = _resolve_host(host, key, port)
    if not conn:
        return

    remote_dir = project or provisioner.load_active_project()
    if not remote_dir:
        rc_error("No active remote project.", hint="Use --project or run sandbox first.")
        return

    console.print(f"\n  [bold]Remote status[/bold] — {conn.label}\n")

    r = conn.test()
    if not r.ok:
        rc_error(f"SSH failed: {r.stderr}")
        return

    state = provisioner.get_sandbox_status(conn, remote_dir)
    if not state:
        rc_warn("No running sandbox found.")
        console.print()
        return

    rc_print(f"  Project: {remote_dir}")
    rc_print(f"  Network: {state.get('network_name', '?')}")

    containers = state.get("containers", {})
    if containers:
        console.print()
        table = Table(show_header=True, header_style="bold cyan", padding=(0, 2))
        table.add_column("Service")
        table.add_column("Container")
        table.add_column("Port")
        table.add_column("Status")

        for name, info in containers.items():
            port_val = str(info.get("host_port", "-")) if info.get("host_port") else "-"
            table.add_row(
                name, info.get("container_id", "?")[:12], port_val, info.get("status", "?")
            )
        console.print(table)

    # Check if containers are actually running
    r = conn.run(
        "docker ps --format '{{.Names}} {{.Status}}' --filter 'label=rc-aux-of' 2>/dev/null | head -20"
    )
    if r.ok and r.stdout:
        console.print()
        rc_print("  Live containers:")
        for line in r.stdout.splitlines():
            rc_print(f"    {line}", style="rc.dim")

    console.print()


