@remote_app.command()
def teardown(
    host: str = typer.Option(
        None, "--host", "-H", help="SSH connection. Uses saved default if omitted."
    ),
    project: str = typer.Option(None, "--project", help="Remote project directory"),
    key: str = typer.Option(None, "--key", "-k", help="Path to SSH private key"),
    port: int = typer.Option(22, "--port", "-p", help="SSH port"),
) -> None:
    """Tear down the remote sandbox and clean up containers."""
    conn = _resolve_host(host, key, port)
    if not conn:
        return

    remote_dir = project or provisioner.load_active_project()
    if not remote_dir:
        rc_error("No active remote project.", hint="Use --project or run sandbox first.")
        return

    console.print(f"\n  [bold]Remote teardown[/bold] — {conn.label}")
    rc_print(f"  Project: {remote_dir}\n", style="rc.dim")

    if not Confirm.ask("  Tear down the remote sandbox?", default=False):
        rc_print("  Cancelled.\n")
        return

    rc_print("  Tearing down...")
    r = provisioner.teardown_sandbox(conn, remote_dir)
    if r.ok:
        rc_success("Sandbox torn down")
        if r.stdout:
            for line in r.stdout.splitlines()[-5:]:
                rc_print(f"    {line}", style="rc.dim")
    else:
        rc_error(f"Teardown issue: {r.stderr or r.stdout}")

    console.print()
