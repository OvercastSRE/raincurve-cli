def _parse_agent_result(output_lines: list[str]) -> dict | None:
    full_output = "\n".join(output_lines)
    for line in reversed(output_lines):
        line = line.strip()
        if line.startswith("{"):
            try:
                return json.loads(line)
            except json.JSONDecodeError:
                pass
    try:
        json_start = full_output.rfind("{")
        json_end = full_output.rfind("}") + 1
        if json_start >= 0 and json_end > json_start:
            return json.loads(full_output[json_start:json_end])
    except json.JSONDecodeError:
        pass
    return None


def _build_coding_agent_prompt(issue: str, result: dict | None, raw_output: str) -> str:
    lines = [
        "# Bug Fix Request",
        "",
        "## Issue",
        issue,
        "",
    ]

    if result and result.get("success"):
        summary = result.get("summary", "")
        findings = result.get("findings", [])

        if summary:
            lines.extend(["## Root Cause Analysis", summary, ""])

        if findings:
            lines.append("## Findings")
            for i, f in enumerate(findings, 1):
                lines.append(
                    f"\n### {i}. [{f.get('severity', '?').upper()}] {f.get('title', 'Untitled')}"
                )
                if f.get("endpoint"):
                    lines.append(f"- **Code path:** {f['endpoint']}")
                if f.get("evidence"):
                    lines.append(f"- **Evidence:** {f['evidence']}")
            lines.append("")

        lines.extend(
            [
                "## Instructions",
                "Fix this bug. The analysis above was generated by an automated agent that:",
                "- Read the source code and traced the logic",
                "- Queried the database to verify data state",
                "- Triggered the bug and observed actual vs expected output",
                "",
                "Apply the minimal fix needed. Do not refactor unrelated code.",
                "",
            ]
        )
    else:
        lines.extend(
            [
                "## Agent Output (raw)",
                "The automated investigation could not produce structured findings.",
                "Raw output from the investigation:",
                "",
                "```",
                raw_output[-3000:] if len(raw_output) > 3000 else raw_output,
                "```",
                "",
                "## Instructions",
                "Investigate and fix the issue described above using the agent's raw output as context.",
                "",
            ]
        )

    return "\n".join(lines)


def _resolve_host(
    host: str | None,
    key: str | None,
    port: int,
) -> RemoteHost | None:
    if host:
        return RemoteHost.from_string(host, key_path=key, port=port)
    saved = provisioner.load_remote_config()
    if saved:
        if key:
            saved.key_path = key
        return saved
    rc_error("No host specified.", hint="Use --host user@ip or run `raincurve remote setup` first.")
    return None


def _ensure_provisioned(conn: RemoteHost) -> bool:
    """Make sure the remote has Docker, raincurve, and LLM configured. Auto-installs if missing."""
    r = conn.test()
    if not r.ok:
        rc_error(f"SSH failed: {r.stderr}")
        return False

    checks = provisioner.check_prerequisites(conn)

    # Docker
    if not checks.get("docker"):
        rc_print("  Docker not found on remote — installing...")
        r = provisioner.install_docker(conn)
        if not r.ok:
            rc_error(f"Docker install failed: {r.stderr}")
            return False
        rc_success("Docker installed")

    # Raincurve
    rc_check = conn.run("export PATH=$HOME/.local/bin:$PATH && raincurve --version 2>/dev/null")
    if not rc_check.ok or "raincurve" not in rc_check.stdout.lower():
        rc_print("  raincurve not found on remote — installing...")
        r = provisioner.install_raincurve(conn)
        if not r.ok:
            rc_warn(f"raincurve install may have issues: {r.stdout[-200:]}")
        else:
            rc_success("raincurve installed")

    # LLM config
    llm_check = conn.run("cat ~/.raincurve/global.json 2>/dev/null")
    if not llm_check.ok or '"api_key"' not in llm_check.stdout:
        cfg = load_global_config()
        provider = cfg.llm.provider or "openrouter"
        api_key = cfg.llm.api_key or cfg.llm.openrouter_api_key or ""
        if api_key:
            rc_print(f"  Configuring LLM ({provider}) on remote...")
            provisioner.configure_llm(conn, provider, api_key)
            rc_success("LLM configured")
        else:
            rc_error("No LLM API key found.", hint="Run `raincurve init` first.")
            return False

    provisioner.save_remote_config(conn)
    return True


def _run_sandbox(
    conn: RemoteHost,
    repo: str,
    env_file: str | None,
    env: list[str],
    json_output: bool,
    yes: bool,
) -> bool:
    """Clone repo, upload env, run sandbox on remote. Returns True on success."""

    # 1. Clone repo
    rc_print("  Cloning repository...")
    ok, remote_dir = provisioner.clone_repo(conn, repo)
    if not ok:
        rc_error(f"Clone failed: {remote_dir}")
        return False
    rc_success(f"Repo at {remote_dir}")

    # 2. Upload env file / vars
    env_vars: dict[str, str] = {}
    if env_file:
        local = Path(env_file)
        if not local.exists():
            rc_error(f"Env file not found: {env_file}")
            return False
        rc_print("  Uploading environment file...")
        r = provisioner.upload_env_file(conn, str(local), remote_dir)
        if not r.ok:
            rc_error(f"Upload failed: {r.stderr}")
            return False
        rc_success("Environment file uploaded")
        for line in local.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                k, v = line.split("=", 1)
                env_vars[k.strip()] = v.strip()

    for e in env:
        if "=" in e:
            k, v = e.split("=", 1)
            env_vars[k] = v

    if env_vars and not env_file:
        provisioner.upload_env_vars(conn, env_vars, remote_dir)

    # 3. Quick remote analysis
    rc_print("  Analyzing project...")
    conn.run(
        f"cd {remote_dir} && "
        "echo '=== FILES ===' && ls -1 && "
        "echo '=== PACKAGE ===' && "
        "(cat package.json 2>/dev/null | head -30 || "
        "cat requirements.txt 2>/dev/null | head -20 || "
        "cat Pipfile 2>/dev/null | head -20 || "
        "cat go.mod 2>/dev/null | head -10 || "
        "cat Gemfile 2>/dev/null | head -20 || "
        "echo 'No package file found') && "
        "echo '=== DOCKER ===' && "
        "(cat Dockerfile 2>/dev/null | head -5 || echo 'No Dockerfile') && "
        "echo '=== ENV ===' && "
        "(cat .env 2>/dev/null | grep -v '^#' | cut -d= -f1 | head -20 || echo 'No .env')"
    )

    # 4. Approval
    if not yes:
        console.print()
        rc_print("[bold]Sandbox Plan:[/bold]")
        console.print()
        rc_print(f"  Repository:  {repo}")
        rc_print(f"  Remote dir:  {remote_dir}")
        rc_print(f"  Host:        {conn.label}")
        if env_vars:
            rc_print(f"  Env vars:    {len(env_vars)} variables")
            for k in list(env_vars.keys())[:8]:
                is_secret = any(s in k.lower() for s in ["key", "secret", "password", "token"])
                val = "****" if is_secret else env_vars[k][:30]
                rc_print(f"               {k}={val}", style="rc.dim")
            if len(env_vars) > 8:
                rc_print(f"               ... and {len(env_vars) - 8} more", style="rc.dim")
        console.print()

        rc_print("[bold]Raincurve will:[/bold]")
        rc_print("  1. Analyze the codebase (detect stack, DBs, external deps)")
        rc_print("  2. Spin up infrastructure (databases, caches, API mocks)")
        rc_print("  3. Build and run the application in Docker")
        rc_print("  4. Seed realistic test data")
        rc_print("  5. Run end-to-end smoke tests")
        console.print()

        if not Confirm.ask("  Proceed?", default=True):
            rc_print("  Cancelled.\n")
            return False

    console.print()
    rc_print("  Starting sandbox (this takes several minutes)...")
    rc_print("  Streaming output from remote:\n", style="rc.dim")
    console.print("  " + "─" * 60)

    # 5. Run sandbox on remote
    sandbox_cmd = f"cd {remote_dir} && export PATH=$HOME/.local/bin:$PATH && raincurve sandbox -y"
    if json_output:
        sandbox_cmd += " --json"

    exit_code = conn.run_stream(
        sandbox_cmd,
        on_line=lambda line: console.print(f"  {line}"),
        timeout=900,
    )

    console.print("  " + "─" * 60)
    console.print()

    provisioner.save_active_project(remote_dir)
    return exit_code == 0


